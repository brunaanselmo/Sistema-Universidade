    private void textCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_textCodigoFocusLost
       String codigo = textCodigo.getText();
        Controlador control = new Controlador();
        Funcionario c = control.buscarFuncCod(codigo);
        if ( c == null){
        textNome.setText("");
        textNivel.setText("");
        textSalario.setText("");
        textCargaHoraria.setText("");
        btnCadastrar.setEnabled(true);
               
        } else {
        textNome.setText(c.getNome());
        textCodigo.setText(c.getCodigo());
        btnCadastrar.setEnabled(false);
        
        
         }        // TODO add your handling code here:
    }//GEN-LAST:event_textCodigoFocusLost

