    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String codigo = textCodigo.getText();
        String nome = textNome.getText();
        String nivel = textNivel.getText();
        int cargaHoraria = Integer.parseInt(textCargaHoraria.getText());
        double salario = Double.parseDouble(textSalario.getText());
        Controlador control = new Controlador();
        control.addSubstituto(codigo, nome, salario, nivel, cargaHoraria);
        btnCadastrar.setEnabled(false);
        textCodigo.requestFocus();
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

